package oop;

public class Point2D {
	double xcoordinate;
	double ycoordinate;
	
	public Point2D(double xcoordinate, double ycoordinate) {
		this.xcoordinate = xcoordinate;
		this.ycoordinate = ycoordinate;
	}
	
	public double getx() {
		return this.xcoordinate;
	}
	
	public double gety() {
		return this.ycoordinate;
	}
}
